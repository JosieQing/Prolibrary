package com.neusoft.bean;

public class Job {
	private Integer jobno;
	private String jname;
	private String jobStyle;
	private Integer jobCompile;
	private Integer deptno;
	public Integer getJobno() {
		return jobno;
	}
	public void setJobno(Integer jobno) {
		this.jobno = jobno;
	}
	public String getJname() {
		return jname;
	}
	public void setJname(String jname) {
		this.jname = jname;
	}
	public String getJobStyle() {
		return jobStyle;
	}
	public void setJobStyle(String jobStyle) {
		this.jobStyle = jobStyle;
	}
	public Integer getJobCompile() {
		return jobCompile;
	}
	public void setJobCompile(Integer jobCompile) {
		this.jobCompile = jobCompile;
	}
	public Integer getDeptno() {
		return deptno;
	}
	public void setDeptno(Integer deptno) {
		this.deptno = deptno;
	}
	
}
